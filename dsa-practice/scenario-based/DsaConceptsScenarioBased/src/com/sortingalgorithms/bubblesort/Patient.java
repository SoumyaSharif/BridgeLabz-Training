package com.sortingalgorithms.bubblesort;

public class Patient {
	
	private String name ;
	private int condition ;
	
	public Patient(String name , int condition) {
		this.name = name ;
		this.condition = condition ;
	}
	
	public String getname() {
		return name ;
	}
	
	public int getcondition() {
		return condition ;
	}

}
